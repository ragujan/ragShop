    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String brand = jComboBox1.getSelectedItem().toString();
        String cat = jComboBox2.getSelectedItem().toString();
        System.out.println(jComboBox1.getSelectedIndex());
        if (name.isEmpty() || name.equals("product_name")) {
            JOptionPane.showMessageDialog(this, "not a valid product name", "warning", JOptionPane.WARNING_MESSAGE);

        } else if (jComboBox1.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "please select a brand", "warning", JOptionPane.WARNING_MESSAGE);

        } else if (jComboBox2.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "please select a category", "warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {
                ResultSet rs = MySql.sq("SELECT * FROM `product` INNER JOIN `brand` ON "
                        + "`brand`.`brand_id`=`product`.`brand_id` WHERE `product_name`='" + name + "' AND `brand_name`='" + brand + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This product already exits", "warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySql.iud("INSERT INTO `product` (`product_name`,`category_id`,`brand_id`)\n"
                            + "SELECT '" + name + "',`category`.`category_id`,`brand`.`brand_id` FROM `category`\n"
                            + "JOIN `brand` \n"
                            + "WHERE `brand`.`brand_name`='" + brand + "' AND `category`.`category_name`='" + cat + "' ");
                    loadProducts();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed
